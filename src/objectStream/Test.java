package objectStream;

/*
In Java, ObjectOutputStream is a class that is part of the java.io package and is used for serializing objects.
Serialization is the process of converting an object into a stream of bytes, which can be easily saved to a file, sent
over a network, or stored in a database. The ObjectOutputStream class allows you to write objects to an output stream.
 */

/*
It's important to note that the classes you intend to serialize must implement the Serializable interface,
and any fields that you don't want to be serialized should be marked as transient
 */

/*
In Java, ObjectInputStream is a class that is part of the java.io package and is used for deserializing objects.
Deserialization is the process of reconstructing an object from a stream of bytes, typically as a result of reading
the object from a file, receiving it over a network, or retrieving it from a database. The ObjectInputStream class
allows you to read and deserialize objects from an input stream.
 */